        double d = 20.5;//Khai báo NhapMonJava.bien d la kieu boolean co gia tri là 20.5
        boolean b= true;// khai NhapMonJava.bien b có kieu boolean la true;
        char c='a';//khai bao NhapMonJava.bien c co kieu la char co gia tri ki tu là a;
